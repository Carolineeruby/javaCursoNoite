package javaNoite;

public class tabuadaFor {

	public static void main(String[] args) {
        int n = 7;
        
        System.out.println("Tabuada do "+ n + ":");
        for(int i = 1; i<=10; i++) {
        	int resultado = n * i;
        			System.out.println(n + "x" + i + "="+ resultado);
        			
        }


	}

}
